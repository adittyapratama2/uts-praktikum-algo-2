# Untuk meminta input kata dari user
kata = input("Masukkan kata: ")

# Untuk meminta input posisi awal (dimulai dari 0) dan panjang karakter
posisi_awal = int(input("Masukkan posisi awal: "))
panjang_karakter = int(input("Masukkan panjang karakter: "))

# Untuk mengambil substring dari kata berdasarkan posisi awal dan panjang karakter
substring = kata[posisi_awal:posisi_awal + panjang_karakter]

# Untuk menampilkan substring yang diambil
print("Substring:", substring)

