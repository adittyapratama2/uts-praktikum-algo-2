# Untuk meminta input kata pertama, kedua, dan ketiga
kata_pertama = input("Masukkan kata pertama: ")
kata_kedua = input("Masukkan kata kedua: ")
kata_ketiga = input("Masukkan kata ketiga: ")

# Untuk menggabungkan ketiga kata menjadi satu kalimat dengan spasi
kalimat_gabungan = kata_pertama + " " + kata_kedua + " " + kata_ketiga

# Untuk menampilkan hasil penggabungan
print("Hasil penggabungan:", kalimat_gabungan)

