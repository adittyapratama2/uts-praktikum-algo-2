#Meminta input kata pertama, kedua, dan ketiga
kata_pertama = input("Masukkan kata pertama anda: ")
kata_kedua = input("Masukkan kata kedua anda: ")
kata_ketiga = input("Masukkan kata ketiga anda: ")

#Menggabungkan ketiga kata menjadi satu kalimat dengan spasi dari program
kalimat_gabungan = kata_pertama + " " + kata_kedua + " " + kata_ketiga

#Menampilkan hasil penggabungannya
print("Hasil dari penggabungannya adalah:", kalimat_gabungan)