# Masukan Kata Pertama, Kedua, Ketiga
kata_1 = str(input("Masukan Kata Pertama: "))+" "
kata_2 = str(input("Masukan Kata Kedua: "))+" "
kata_3 = str(input("Masukan Kata Ketiga:"))
