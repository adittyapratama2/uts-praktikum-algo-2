# Program untuk menggabungkan tiga kata

# Meminta input kata pertama, kedua, dan ketiga
kata_pertama = input("Masukkan kata pertama: ")
kata_kedua = input("Masukkan kata kedua: ")
kata_ketiga = input("Masukkan kata ketiga: ")

# Menggabungkan ketiga kata dengan spasi
kalimat_gabungan = kata_pertama + " " + kata_kedua + " " + kata_ketiga

# Menampilkan hasil penggabungan
print(f"Hasil penggabungan: {kalimat_gabungan}")
