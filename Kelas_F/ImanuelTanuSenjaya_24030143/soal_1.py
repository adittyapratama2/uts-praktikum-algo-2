# Program untuk menghitung luas lingkaran

# Mendefinisikan nilai Ï€ (pi)
pi = 3.14

# Meminta input jari-jari dari pengguna
jari_jari = float(input("Masukkan jari-jari: "))

# Menghitung luas lingkaran
luas_lingkaran = pi * (jari_jari ** 2)

# Menampilkan hasil perhitungan
print(f"Luas lingkaran: {luas_lingkaran:.2f}")
