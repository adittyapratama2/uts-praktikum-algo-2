# Meminta pengguna memasukkan tiga kata
kata_pertama = input("Masukkan kata pertama: ")
kata_kedua = input("Masukkan kata kedua: ")
kata_ketiga = input("Masukkan kata ketiga: ")

# Menggabungkan ketiga kata dengan spasi di antara setiap kata
kalimat_gabungan = f"{kata_pertama} {kata_kedua} {kata_ketiga}"

# Menampilkan hasil penggabungan
print("Hasil penggabungan:", kalimat_gabungan)

