# Program untuk menggabungkan tiga kata menjadi satu kalimat

# Meminta input dari pengguna
kata1 = input("Masukkan kata pertama: ")
kata2 = input("Masukkan kata kedua: ")
kata3 = input("Masukkan kata ketiga: ")

# Menggabungkan ketiga kata dengan spasi di antaranya
kalimat = f"{kata1} {kata2} {kata3}"

# Menampilkan hasil
print(f"Kalimat gabungan: {kalimat}")

