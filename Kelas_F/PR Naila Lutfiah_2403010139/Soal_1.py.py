#Program menghitung luas lingkaran
#nilai pi
pi = 3.14

# input dari pengguna
jari_jari = float(input("Masukan jari-jari Lingkaran:"))

#menghitung luas lingkaran
Luas_Lingkaran = pi * (jari-jari ** 2)

#menampilkan hasil
print(f"Luas Lingkaran = {Luas_Lingkaran}")
                        
