# Meminta pengguna untuk memasukkan jari-jari lingkaran
radius = float(input("Masukkan jari-jari: "))

# Menggunakan nilai π (pi) sebesar 3.14
pi = 3.14

# Menghitung luas lingkaran
area = pi * radius ** 2

# Menampilkan hasil
print("Luas lingkaran:", area)

